"""Top 5 Coins – Historical Prices"""

import requests
import pandas as pd
import time

coin_ids = ["bitcoin","ethereum","tether","binancecoin","ripple"]

top_historical = []

for coin in coin_ids:

    url = f"https://api.coingecko.com/api/v3/coins/{coin}/market_chart?vs_currency=usd&days=365"

    while True:
        response = requests.get(url)

        if response.status_code == 200:
            coin_data = response.json()

            for price_data in coin_data["prices"]:
                date = pd.to_datetime(price_data[0], unit="ms").date()
                price = price_data[1]

                top_historical.append({
                    "coin_id": coin,
                    "date": date,
                    "price_inr": price
                })

            print(f"{coin} data collected")
            break

        elif response.status_code == 429:
            print("Rate limit hit. Waiting 60 seconds...")
            time.sleep(60)

        else:
            print("API Error:", response.status_code)
            break

    time.sleep(5)
Top_5_Historical_df = pd.DataFrame(top_historical)

Top_5_Historical_df.to_csv("Top_5_Historical_df.csv",index = False)

