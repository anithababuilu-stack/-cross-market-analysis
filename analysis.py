"""4.Find days when oil price spiked and compare with Bitcoin price change.

"""

import sqlite3
import pandas as pd

connection = sqlite3.connect("Market.db")

Join_query4= """
WITH Oil_Returns AS (
    SELECT
        Date,
        Price,
        LAG(Price) OVER (ORDER BY Date) AS prev_price,
        ((Price - LAG(Price) OVER (ORDER BY Date)) / LAG(Price) OVER (ORDER BY Date)) * 100 AS oil_return
    FROM Oil_prices
    WHERE Date BETWEEN '2025-01-01' AND '2025-12-31'
)
SELECT
    oil.Date,
    oil.Price AS oil_price,
    oil.oil_return,
    crp.price_inr AS bitcoin_price
FROM Oil_Returns oil
LEFT JOIN Crypto_Prices crp ON oil.Date=crp.date
AND crp.coin_id = 'bitcoin'

WHERE oil.oil_return > 5
ORDER BY oil.Date;
"""

oil_bitcoin_df = pd.read_sql(Join_query4, connection)


